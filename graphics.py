def showboard():
    board = list(empty_board)
    i = 0
    for x,y in piece_pos_map:
        board[y*35 + x] = str(i)
        i += 1
        i %= 10
    for map in exit_pos_map:
        i = 0
        for x,y in map:
            board[y*35 + x] = str(i)
            i += 1

    print("".join(board))

